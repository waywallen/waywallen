//! C codegen for a parsed `Protocol`.
//!
//! Phase 2 output: a single header + a single implementation file that
//! any C consumer can compile against. Both files depend only on
//! `<stdint.h>`, `<stdlib.h>`, and `<string.h>` — no libc extensions,
//! no pthread, no OS syscalls. Wire I/O and SCM_RIGHTS are the
//! libwaywallen_display `codec` layer's job, not the generated code's.
//!
//! Naming:
//!   - Structs, functions, and constants are snake_case with the
//!     `ww_` prefix.
//!   - Request messages become `ww_req_<name>_t`.
//!   - Event messages become `ww_evt_<name>_t`.
//!   - Opcode enum values: `WW_REQ_<NAME>` / `WW_EVT_<NAME>`.

use crate::parser::{ArgType, FdSpec, Message, Protocol};
use std::fmt::Write;

// ---------------------------------------------------------------------------
// Public entry points
// ---------------------------------------------------------------------------

pub fn emit_header(p: &Protocol) -> String {
    let mut out = String::new();
    emit_header_prologue(&mut out, p);
    emit_common_types(&mut out);
    emit_opcodes(&mut out, p);
    for m in &p.requests {
        emit_message_struct(&mut out, m, MsgKind::Request);
    }
    for m in &p.events {
        emit_message_struct(&mut out, m, MsgKind::Event);
    }
    emit_function_decls(&mut out, p);
    emit_header_epilogue(&mut out);
    out
}

// ---------------------------------------------------------------------------
// Header
// ---------------------------------------------------------------------------

#[derive(Copy, Clone, PartialEq, Eq)]
enum MsgKind {
    Request,
    Event,
}

impl MsgKind {
    fn prefix(self) -> &'static str {
        match self {
            Self::Request => "ww_req_",
            Self::Event => "ww_evt_",
        }
    }
    fn enum_prefix(self) -> &'static str {
        match self {
            Self::Request => "WW_REQ_",
            Self::Event => "WW_EVT_",
        }
    }
}

fn emit_header_prologue(out: &mut String, p: &Protocol) {
    writeln!(out, "/* AUTO-GENERATED by wayproto-gen. DO NOT EDIT BY HAND. */").unwrap();
    writeln!(out, "/* Source: protocol/{}_v{}.xml */", p.name, p.version).unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#ifndef WAYWALLEN_PROTO_H").unwrap();
    writeln!(out, "#define WAYWALLEN_PROTO_H").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#include <stdint.h>").unwrap();
    writeln!(out, "#include <stddef.h>").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#ifdef __cplusplus").unwrap();
    writeln!(out, "extern \"C\" {{").unwrap();
    writeln!(out, "#endif").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "#define WW_PROTOCOL_NAME    \"{}-v{}\"",
        p.name.replace('_', "-"),
        p.version
    )
    .unwrap();
    writeln!(out, "#define WW_PROTOCOL_VERSION {}", p.version).unwrap();
    writeln!(out).unwrap();
}

fn emit_header_epilogue(out: &mut String) {
    writeln!(out).unwrap();
    writeln!(out, "#ifdef __cplusplus").unwrap();
    writeln!(out, "}} /* extern \"C\" */").unwrap();
    writeln!(out, "#endif").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#endif /* WAYWALLEN_PROTO_H */").unwrap();
}

fn emit_common_types(out: &mut String) {
    out.push_str(
        r#"/* --- Shared primitive types --- */

typedef struct ww_rect {
    float x;
    float y;
    float w;
    float h;
} ww_rect_t;

typedef struct ww_array_u32 {
    uint32_t count;
    uint32_t *data;    /* heap-allocated by decoder; NULL if count==0 */
} ww_array_u32_t;

typedef struct ww_array_u64 {
    uint32_t count;
    uint64_t *data;
} ww_array_u64_t;

typedef struct ww_array_i32 {
    uint32_t count;
    int32_t *data;
} ww_array_i32_t;

typedef struct ww_array_i64 {
    uint32_t count;
    int64_t *data;
} ww_array_i64_t;

typedef struct ww_array_f32 {
    uint32_t count;
    float *data;
} ww_array_f32_t;

typedef struct ww_array_f64 {
    uint32_t count;
    double *data;
} ww_array_f64_t;

typedef struct ww_array_string {
    uint32_t count;
    char **data;       /* each entry NUL-terminated, heap-allocated */
} ww_array_string_t;

typedef struct ww_kv {
    char *key;
    char *value;
} ww_kv_t;

typedef struct ww_kv_list {
    uint32_t count;
    ww_kv_t *data;
} ww_kv_list_t;

/* --- Codec buffer + return codes --- */

typedef struct ww_buf {
    uint8_t *data;
    size_t len;
    size_t cap;
} ww_buf_t;

/* Return codes: 0 = success, negative = -errno-ish. */
#define WW_OK                  0
#define WW_ERR_SHORT           (-1)   /* buffer truncated */
#define WW_ERR_BAD_STRING      (-2)   /* malformed length-prefixed string */
#define WW_ERR_BAD_ARRAY       (-3)   /* array length overflow */
#define WW_ERR_TRAILING        (-4)   /* unexpected trailing bytes */
#define WW_ERR_UNKNOWN_OPCODE  (-5)
#define WW_ERR_NOMEM           (-6)
#define WW_ERR_OVERFLOW        (-7)   /* frame bigger than u16 can address */

"#,
    );
}

fn emit_opcodes(out: &mut String, p: &Protocol) {
    writeln!(out, "/* --- Opcodes --- */").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "typedef enum ww_request_op {{").unwrap();
    for m in &p.requests {
        writeln!(
            out,
            "    {}{} = {},",
            MsgKind::Request.enum_prefix(),
            m.name.to_uppercase(),
            m.opcode
        )
        .unwrap();
    }
    writeln!(out, "}} ww_request_op_t;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "typedef enum ww_event_op {{").unwrap();
    for m in &p.events {
        writeln!(
            out,
            "    {}{} = {},",
            MsgKind::Event.enum_prefix(),
            m.name.to_uppercase(),
            m.opcode
        )
        .unwrap();
    }
    writeln!(out, "}} ww_event_op_t;").unwrap();
    writeln!(out).unwrap();
}

fn emit_message_struct(out: &mut String, m: &Message, kind: MsgKind) {
    let type_name = message_type_name(m, kind);
    writeln!(out, "typedef struct {} {{", type_name).unwrap();
    if m.args.is_empty() {
        writeln!(out, "    int _empty; /* C forbids empty structs */").unwrap();
    } else {
        for a in &m.args {
            let ty = c_type(&a.ty);
            let field = c_field_name(&a.name);
            // Pointer types render `char *name;` (star glued to name);
            // everything else renders `uint32_t name;`.
            if ty.ends_with('*') {
                writeln!(out, "    {ty}{field};").unwrap();
            } else {
                writeln!(out, "    {ty} {field};").unwrap();
            }
        }
    }
    writeln!(out, "}} {};", type_name).unwrap();
    writeln!(out).unwrap();
}

fn emit_function_decls(out: &mut String, p: &Protocol) {
    writeln!(out, "/* --- Per-message functions ---").unwrap();
    writeln!(
        out,
        " * encode:        append wire body to `out` (header is the caller's job)"
    )
    .unwrap();
    writeln!(
        out,
        " * decode:        consume `len` bytes; on success owns heap allocations"
    )
    .unwrap();
    writeln!(
        out,
        " * free:          release any heap fields the decoder populated"
    )
    .unwrap();
    writeln!(
        out,
        " * expected_fds:  how many SCM_RIGHTS fds the wire frame must carry"
    )
    .unwrap();
    writeln!(out, " */").unwrap();
    writeln!(out).unwrap();

    for m in &p.requests {
        emit_message_decls(out, m, MsgKind::Request);
    }
    for m in &p.events {
        emit_message_decls(out, m, MsgKind::Event);
    }

    // ww_buf helpers.
    writeln!(out, "/* --- Output buffer helpers --- */").unwrap();
    writeln!(out, "void ww_buf_init(ww_buf_t *b);").unwrap();
    writeln!(out, "void ww_buf_free(ww_buf_t *b);").unwrap();
    writeln!(out, "int  ww_buf_reserve(ww_buf_t *b, size_t additional);").unwrap();
    writeln!(out).unwrap();
}

fn emit_message_decls(out: &mut String, m: &Message, kind: MsgKind) {
    let type_name = message_type_name(m, kind);
    let fn_prefix = type_name.trim_end_matches("_t");
    writeln!(
        out,
        "int  {fn_prefix}_encode(const {type_name} *m, ww_buf_t *out);"
    )
    .unwrap();
    writeln!(
        out,
        "int  {fn_prefix}_decode(const uint8_t *buf, size_t len, {type_name} *out);"
    )
    .unwrap();
    writeln!(out, "void {fn_prefix}_free({type_name} *m);").unwrap();
    let fd_signature = match &m.fds {
        FdSpec::None | FdSpec::Fixed(_) => {
            format!(
                "uint32_t {fn_prefix}_expected_fds(const {type_name} *m);"
            )
        }
        FdSpec::Product(_) => {
            format!(
                "uint32_t {fn_prefix}_expected_fds(const {type_name} *m);"
            )
        }
    };
    writeln!(out, "{fd_signature}").unwrap();
    writeln!(out).unwrap();
}

