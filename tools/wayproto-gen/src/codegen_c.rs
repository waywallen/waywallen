#[cfg(test)]
mod tests {
    use super::*;
    use crate::parser;

    fn parse(src: &str) -> Protocol {
        parser::parse_protocol(src).unwrap()
    }

    #[test]
    fn header_contains_protocol_macros() {
        let p = parse(
            r#"<protocol name="foo" version="1">
                <request name="hello" opcode="1">
                    <arg name="x" type="u32"/>
                </request>
            </protocol>"#,
        );
        let h = emit_header(&p);
        assert!(h.contains("#define WW_PROTOCOL_NAME    \"foo-v1\""));
        assert!(h.contains("#define WW_PROTOCOL_VERSION 1"));
        assert!(h.contains("WW_REQ_HELLO = 1"));
        assert!(h.contains("typedef struct ww_req_hello"));
        assert!(h.contains("ww_req_hello_encode"));
        assert!(h.contains("ww_req_hello_decode"));
        assert!(h.contains("ww_req_hello_free"));
        assert!(h.contains("ww_req_hello_expected_fds"));
    }

    #[test]
    fn header_events_render() {
        let p = parse(
            r#"<protocol name="t" version="1">
                <event name="welcome" opcode="1">
                    <arg name="server_version" type="string"/>
                    <arg name="features" type="array" element="string"/>
                </event>
            </protocol>"#,
        );
        let h = emit_header(&p);
        assert!(h.contains("WW_EVT_WELCOME = 1"));
        assert!(h.contains("char *server_version"));
        assert!(h.contains("ww_array_string_t features"));
    }

    #[test]
    fn header_rect_type_present() {
        let p = parse(
            r#"<protocol name="t" version="1">
                <event name="cfg" opcode="1">
                    <arg name="r" type="rect"/>
                </event>
            </protocol>"#,
        );
        let h = emit_header(&p);
        assert!(h.contains("typedef struct ww_rect"));
        assert!(h.contains("ww_rect_t r;"));
    }
}
